import request from "supertest";
import { app } from "../../app";

it('fails when the email doesn\'t exist ', async () => {
  return request(app)
    .post('/api/users/signin')
    .send({
      email: 'test@test.com',
      password: 'password'
    })
    .expect(400);
});

it('returns a 201 on successful signup and 200 after signin, checks that there is a cookie', async () => {
  await request(app)
    .post('/api/users/signup')
    .send({
      email: 'test@test.com',
      password: 'password'
    })
    .expect(201);
  const resp = await request(app)
    .post('/api/users/signin')
    .send({
      email: 'test@test.com',
      password: 'password'
    })
    .expect(200);
    expect(resp.get('Set-Cookie')).toBeDefined();
});

it('returns a 201 on successful signup and 400 after failing signin', async () => {
  await request(app)
    .post('/api/users/signup')
    .send({
      email: 'test@test.com',
      password: 'password'
    })
    .expect(201);
  await request(app)
    .post('/api/users/signin')
    .send({
      email: 'test@test.com',
      password: 'password3'
    })
    .expect(400);
});

